def print_line(char="-", length=65):
    print(char * length)


def get_result(score):
    for threshold, state, recommendation in states:
        if score <= threshold:
            return state, recommendation
    return states[-1][1], states[-1][2]


def run_survey():
    print_line("=")
    print("   MONTHLY ACADEMIC SKILL AUDIT")
    print("   Targeted Improvement Plan Questionnaire")
    print("   Topic #180 | WIUT | Fundamentals of Programming")
    print_line("=")
    print()
    print(" Instructions:")
    print(" For each question, type the number (0-4) next to your answer.")
    print(" 0 = most frequent / best habit   |   4 = least frequent / worst habit")
    print(" There are no right or wrong answers. Answer honestly.")
    print()
    print_line()

    total_score = 0
    current_section = ""

    for i, q in enumerate(questions):
        if "section" in q and q["section"] != current_section:
            current_section = q["section"]
            print()
            print(f"  {'=' * 55}")
            print(f"  {current_section}")
            print(f"  {'=' * 55}")
            print()

        print(q["question"])
        print()
        for opt in q["options"]:
            print(f"   {opt}")
        print()

        while True:
            try:
                answer = int(input("   Your answer (0-4): "))
                if 0 <= answer <= 4:
                    total_score += answer
                    break
                else:
                    print("   Please enter a number between 0 and 4.")
            except ValueError:
                print("   Invalid input. Please enter a number between 0 and 4.")

        print()
        if i < len(questions) - 1:
            print_line()

    # Results
    print()
    print_line("=")
    print("   YOUR RESULTS")
    print_line("=")
    print()

    state, recommendation = get_result(total_score)
    max_score = len(questions) * 4

    print(f"   Total Score  :  {total_score} / {max_score}")
    print()
    print(f"   Your State   :  {state}")
    print()
    print_line()
    print()
    print("   Recommendation:")
    print(f"   {recommendation}")
    print()
    print_line("=")
    print("   Thank you for completing the survey.")
    print("   Use this result as a starting point for reflection,")
    print("   not as a final judgment of your abilities.")
    print_line("=")
    print()


if __name__ == "__main__":
    run_survey()
